    private boolean checkParameters() {
        //algum campo de texto (sem ser id)ta vazio
        if (txtName.getText().isEmpty() || txtLogin.getText().isEmpty() || txtPass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Algum campo está vazio.");
            return false;
        }
        return true;
    }

    private void populateUsersTable() {
        DefaultTableModel model = (DefaultTableModel) tableUsers.getModel();
        model.setNumRows(0);

        userDao dao = new userDao();

        for (User u : dao.list()) {
            model.addRow(new Object[]{
                u.getIdUser(),
                u.getNameUser(),
                u.getLoginUser(),
                u.getPassUser()
            });

        }
    }

    private void populateUsersTableCustom() {
        DefaultTableModel model = (DefaultTableModel) tableUsers.getModel();
        model.setNumRows(0);

        userDao dao = new userDao();

        for (User u : dao.searchForValue(String.valueOf(comboSearch.getSelectedItem()), txtSearch.getText())) {
            model.addRow(new Object[]{
                u.getIdUser(),
                u.getNameUser(),
                u.getLoginUser(),
                u.getPassUser()
            });

        }
    }

    private void updateControls(PHASE phase) {
        switch (phase) {
            case NONE:
                //sem treco de texto
                txtName.setEditable(false);
                txtLogin.setEditable(false);
                txtPass.setEditable(false);
                //só novo user pode ser feito
                btNew.setEnabled(true);
                btSave.setEnabled(false);
                btEdit.setEnabled(false);
                btDelete.setEnabled(false);
                btCancel.setEnabled(false);

                txtId.setText("");
                txtName.setText("");
                txtLogin.setText("");
                txtPass.setText("");
                break;

            case NEW:
                //pode colocar novo treco
                txtName.setEditable(true);
                txtLogin.setEditable(true);
                txtPass.setEditable(true);
                //cancelar ou criar novo
                btNew.setEnabled(false);
                btSave.setEnabled(true);
                btEdit.setEnabled(false);
                btDelete.setEnabled(false);
                btCancel.setEnabled(true);
                break;

            case VIEW:
                //só vendo, veio da tabela
                //nao pode editar
                txtName.setEditable(false);
                txtLogin.setEditable(false);
                txtPass.setEditable(false);
                //deletar o usuario ou editar
                btNew.setEnabled(false);
                btSave.setEnabled(false);
                btEdit.setEnabled(true);
                btDelete.setEnabled(true);
                btCancel.setEnabled(true);
                break;

            case EDIT:
                //pode editar
                txtName.setEditable(true);
                txtLogin.setEditable(true);
                txtPass.setEditable(true);
                //salvar o user editado ou cancelar
                btNew.setEnabled(false);
                btSave.setEnabled(true);
                btEdit.setEnabled(false);
                btDelete.setEnabled(false);
                btCancel.setEnabled(true);
                break;

        }
    }

