    private void btSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSaveMouseClicked
        if (checkParameters()) {
            User u = new User();
            userDao dao = new userDao();
            switch (registerPhase) {
                case NEW:
                    u.setNameUser(txtName.getText());
                    u.setLoginUser(txtLogin.getText());
                    u.setPassUser(txtPass.getText());

                    dao.create(u); //cria o treco
                    break;

                case EDIT:
                    u.setIdUser(Integer.parseInt(txtId.getText()));
                    u.setNameUser(txtName.getText());
                    u.setLoginUser(txtLogin.getText());
                    u.setPassUser(txtPass.getText());

                    dao.update(u); //atualiza o user

                    break;
            }

            registerPhase = PHASE.NONE;
            updateControls(registerPhase);
            populateUsersTable();
        }
    }//GEN-LAST:event_btSaveMouseClicked

    private void tableUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUsersMouseClicked

        if (tableUsers.getSelectedRow() != -1) {

            txtId.setText(tableUsers.getValueAt(tableUsers.getSelectedRow(), 0).toString());
            txtName.setText(tableUsers.getValueAt(tableUsers.getSelectedRow(), 1).toString());
            txtLogin.setText(tableUsers.getValueAt(tableUsers.getSelectedRow(), 2).toString());
            txtPass.setText(tableUsers.getValueAt(tableUsers.getSelectedRow(), 3).toString());

            registerPhase = PHASE.VIEW;
            updateControls(registerPhase);

            tabcUsers.setSelectedIndex(0);
        }
    }//GEN-LAST:event_tableUsersMouseClicked

    private void btDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btDeleteMouseClicked
        User u = new User();
        userDao dao = new userDao();

        u.setIdUser(Integer.parseInt(txtId.getText()));
        u.setNameUser(txtName.getText());
        u.setLoginUser(txtLogin.getText());
        u.setPassUser(txtPass.getText());

        dao.delete(u); //deleta o user

        registerPhase = PHASE.NONE;
        updateControls(registerPhase);

        populateUsersTable();
    }//GEN-LAST:event_btDeleteMouseClicked

